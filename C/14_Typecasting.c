#include <stdio.h>

int main() {
    int a=9;
    float b=64;
    printf("value of a is %d\n ", (int) b); // here we are converting  float value to int it is called typecasting
    printf("value of a is %f\n ", (float) a); // here we are converting int  value to float it is called typecasting
    return 0;
}